    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String s1 = "";
        
        jTextArea1.setEditable(false);
        
        
        s1 = jTextField1.getText();
        
         Random r = new Random();
        
        String[] words = s1.split("\\s");

        
        String possibleFirstWords[] = {"Hello","Hi", "Hey", "Whats Up"};
        String possibleQuestions[] = {"Wanna hang out","Whats your fav tv show?","Is your name, Bhorade?","How are you","Whats the time right now?","Are you smart?"};
        
        String timeStamp = new SimpleDateFormat("yyyy-MM-dd HH-mm-ss").format(Calendar.getInstance().getTime());
        
        String possibleAnswers[] = {"Sure","Young Sheldon","Hahahaha. ITS a secret!","I m Fine, Thank You!",timeStamp,"No, dumb as a machine!"};
        
        int randomInt = r.nextInt(possibleFirstWords.length) + 0;

        if(evt.getSource()==jButton1){
            
            if(s1.contains("exit"))
            {
                setVisible(false);
            }
            if(s1.contains("game"))
            {
                playGames = true;
                if(playGames){
                    playGame();
                }
            }
            for (String possibleFirstWord : possibleFirstWords) {
                if (s1.toLowerCase().contains(possibleFirstWord.toLowerCase())) {
                    jTextArea1.setText(possibleFirstWords[randomInt]+", "+uname);
                }
            }
            for(int j=0;j<possibleFirstWords.length;j++){
                    if(possibleQuestions[j].trim().equalsIgnoreCase(s1.trim())){
                        jTextArea1.setText(possibleAnswers[j]+", "+uname);
                    }
            }
                for(int j=0;j<possibleFirstWords.length;j++){
                    String temp = customQ.get(j);
                    String temp1 = customA.get(j);
                    if(temp.equalsIgnoreCase(s1)){
                        jTextArea1.setText(temp1);
                        indexCQA++;
                    }
                }
               // }
            }
    }//GEN-LAST:event_jButton1ActionPerformed

