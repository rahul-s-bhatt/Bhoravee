    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         
        String s1 = "";        
        s1 = jTextField1.getText();
      
        String s2 = jTextArea1.getText();
        
        String s3 = "";
        
        jTextArea1.setEditable(false);
        
        Random r = new Random();
        
        String[] words = s1.split("\\s");
        
        String possibleGoodResponses[] = {"Good","Fine", "Great", "Well"};
        
        String possibleBadResponses[] = {"Not","Ill", "Disturbed", "OK"};
        
        String askQuestions[] = {"How are you","Do you want to play a game ?"};
        String getAnswers[] = {"Great, Glad to hear that!","Ohhh! Poor you, go listen to some music and relax!"};
        
        
        int askRandomQ = r.nextInt(askQuestions.length) + 0;
        
        
         if(evt.getSource()==jButton2){
             
            if(s1.isEmpty() || s1.contains("ask")){
                jTextArea1.setText(askQuestions[askRandomQ]);
            }
            for(int i=0;i<askQuestions.length;i++){
                    if(s1.toLowerCase().contains(possibleGoodResponses[i].toLowerCase())){
                        jTextArea1.setText(getAnswers[0]);
                    }
                    if(s1.toLowerCase().contains(possibleBadResponses[i].toLowerCase())){
                        jTextArea1.setText(getAnswers[1]);
                    }
            }
            for(int i=0;i<askQuestions.length;i++){

                    if(s2.contains("game")){
                        if(words[i].equalsIgnoreCase("yes"))
                        {
                            playGames = true;
                            if(playGames){
                            playGame();
                            }   
                        }
                        else {
                            jTextArea1.setText("Ask me something else!");
                        }
                        
                    }
                }
            }
    }//GEN-LAST:event_jButton2ActionPerformed

